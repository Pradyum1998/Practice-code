package com.tablejoin.entity;

import jakarta.persistence.*;

@Entity
@Table(name = "Leads")
public class Lead {
    @Id
    @GeneratedValue
    private int id;
    private String name;
    private int age;

    public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public int getAge() {
        return age;
    }

    public void setAge(int age) {
        this.age = age;
    }
}
